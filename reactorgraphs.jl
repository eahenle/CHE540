# ╔═╡ 984a73da-b1e1-408b-8ed5-b2028dcfb048
function ReactorGraph(reactors...)
	nb_reactors = length(reactors)
	reactor_graph = MetaDiGraph(nb_reactors+2)
	set_prop!(reactor_graph, 1, :nodetype, :INPUT)
	set_prop!(reactor_graph, 4, :nodetype, :OUTPUT)
	for i in 2:nb_reactors+1
		set_prop!(reactor_graph, i, :nodetype, :REACTOR)
		set_prop!(reactor_graph, i, :reactor, reactors[i-1])
	end
	for i in 1:nb_reactors+1
		add_edge!(reactor_graph, i, i+1)
	end
	return reactor_graph
end;

# ╔═╡ a6d996b3-399c-4851-b5af-d8ff89c68d3a
function levenspiel_area(reactor_graph, i, w, X₀, X)
	reactor = get_prop(reactor_graph, i+1, :reactor)(w)
	area = reactor.area(X₀, X)
	return area
end;

# ╔═╡ a10a7d80-c06f-4f5d-8beb-242669e89ac2
function levenspiel_areas(reactor_graph, weights)
	return [levenspiel_area(reactor_graph, i, w) for (i, w) in enumerate(weights)]
end;

# ╔═╡ d63df47b-5e7f-4a4e-b92a-5d42f972515b
function optimal_weights(reactor_graph, reaction, FA₀, X)
	weight(weights) = FA₀ * sum(levenspiel_areas(reactor_graph, weights))
	return optimize(weight, [0., 0.])
end;

